#!/usr/bin/env node
import fs from 'node:fs';
import path from 'node:path';
import { fileURLToPath } from 'node:url';

const root = path.resolve(path.dirname(fileURLToPath(import.meta.url)), '..');
const sourceFile = 'data/default-user/re0-engine/characters/CHARACTER_PLAYLOOP_ACTIVE_HOOKS_2026-05-29.json';
const outputFile = 'public/scripts/extensions/third-party/re0-adventure-engine/data/character-playloop-hooks.generated.js';

function readJson(relativePath) {
    return JSON.parse(fs.readFileSync(path.join(root, relativePath), 'utf8'));
}

function cleanText(value, limit = 220) {
    return String(value || '')
        .replace(/486\/Subaru/gi, 'player-owned inherited')
        .replace(/\bSubaru\b/gi, 'player-owned inherited causality')
        .replace(/\bNatsuki\b/gi, 'player-owned inherited causality')
        .replace(/菜月昴/g, '玩家继承因果')
        .replace(/486[- ]?causality/gi, 'player-owned inherited causality')
        .replace(/inherited 486 causality/gi, 'player-owned inherited causality')
        .replace(/inherited player-owned inherited/gi, 'player-owned inherited')
        .replace(/protagonist-owned player-owned inherited causality/gi, 'protagonist-owned inherited causality')
        .replace(/开局因果源/g, '玩家继承因果')
        .replace(/开局因果/g, '玩家继承因果')
        .replace(/\s+/g, ' ')
        .trim()
        .slice(0, limit);
}

function cleanArray(items, limit = 6, itemLimit = 160) {
    return [...new Set((Array.isArray(items) ? items : []).map((item) => cleanText(item, itemLimit)).filter(Boolean))].slice(0, limit);
}

const source = readJson(sourceFile);
const hooksByCharacter = {};

for (const hook of source.hooks || []) {
    const characterId = cleanText(hook.characterId, 80);
    const mode = cleanText(hook.behaviorMode, 40);
    if (!characterId || !mode) {
        continue;
    }
    hooksByCharacter[characterId] ??= {};
    hooksByCharacter[characterId][mode] = {
        hookId: cleanText(hook.hookId, 100),
        characterId,
        displayName: cleanText(hook.displayName, 80),
        behaviorMode: mode,
        priority: cleanText(hook.priority, 16),
        stageSlot: cleanText(hook.stageSlot, 80),
        stageLayer: cleanText(hook.stageLayer, 80),
        activeAction: cleanText(hook.activeAction, 150),
        playerFacingPromptStyle: cleanText(hook.playerFacingPromptStyle, 130),
        activationTriggers: cleanArray(hook.activationTriggers, 6, 130),
        allowedOutputs: cleanArray(hook.allowedOutputs, 5, 80),
        stateEffectHints: cleanArray(hook.stateEffectHints, 5, 90),
        cooldownPolicy: cleanText(hook.cooldownPolicy, 120),
        stageRequirements: {
            expression: cleanText(hook.stageRequirements?.expression, 50),
            pose: cleanText(hook.stageRequirements?.pose, 50),
            outfit: cleanText(hook.stageRequirements?.outfit, 50),
        },
    };
}

const metadata = {
    generatedAt: new Date().toISOString(),
    sourceFile,
    characterCount: Object.keys(hooksByCharacter).length,
    hookCount: Object.values(hooksByCharacter).reduce((sum, modes) => sum + Object.keys(modes).length, 0),
    modes: [...new Set(Object.values(hooksByCharacter).flatMap((modes) => Object.keys(modes)))].sort(),
    causalityPolicy: '玩家是唯一主动因果承载者；死亡回归压力、阵营入口和开局因果全部绑定玩家，不存在第二主角。',
};

const output = `/* eslint-disable */
// Generated by scripts/build-re0-character-playloop-hooks.mjs. Do not edit by hand.
export const characterPlayLoopHookMetadata = ${JSON.stringify(metadata, null, 4)};

export const characterPlayLoopHooks = ${JSON.stringify(hooksByCharacter, null, 4)};
`;

fs.writeFileSync(path.join(root, outputFile), output);
console.log(JSON.stringify({ outputFile, ...metadata }, null, 2));
