#!/usr/bin/env node
import fs from 'node:fs';
import path from 'node:path';

const root = path.resolve(new URL('..', import.meta.url).pathname);
const sourceFiles = [
    'data/default-user/re0-engine/characters/CHARACTER_DOSSIER_MERGE_PLAN_2026-05-28.json',
    'data/default-user/re0-engine/characters/CHARACTER_DOSSIER_MERGE_PLAN_REST_2026-05-28.json',
];
const outputFile = 'public/scripts/extensions/third-party/re0-adventure-engine/data/character-dossier-static-patch.generated.js';

const runtimeFieldDenylist = [
    'memory',
    'personality',
    'attitudeToPlayer',
    'trust',
    'suspicion',
    'trauma',
    'affection',
    'desire',
    'intimacyExperience',
    'intimacyHistory',
    'sexualKinks',
    'hobbies',
    'flags',
    'arcLog',
    'lastUpdatedDay',
];

function readJson(relativePath) {
    return JSON.parse(fs.readFileSync(path.join(root, relativePath), 'utf8'));
}

function validateCharacterPlan(id, entry) {
    if (!entry || typeof entry !== 'object') {
        throw new Error(`Invalid character dossier plan for ${id}`);
    }
    if (!entry.staticPatch || typeof entry.staticPatch !== 'object') {
        throw new Error(`Missing staticPatch for ${id}`);
    }
    const denied = runtimeFieldDenylist.filter((field) => Object.hasOwn(entry.staticPatch, field));
    if (denied.length) {
        throw new Error(`staticPatch for ${id} contains runtime fields: ${denied.join(', ')}`);
    }
}

const characters = {};

for (const sourceFile of sourceFiles) {
    const source = readJson(sourceFile);
    for (const [id, entry] of Object.entries(source.characters || {})) {
        if (characters[id]) {
            throw new Error(`Duplicate static dossier id ${id} from ${sourceFile}`);
        }
        validateCharacterPlan(id, entry);
        characters[id] = {
            id,
            displayName: entry.displayName || id,
            requestAlias: entry.requestAlias || id,
            sourceFile,
            staticPatch: entry.staticPatch,
            routeHooks: Array.isArray(entry.routeHooks) ? entry.routeHooks : [],
            vnDialogueOwnershipHints: entry.vnDialogueOwnershipHints || {},
            spriteNeeds: entry.spriteNeeds || entry.staticPatch?.spriteNeeds || {},
            adultModeCompatibility: entry.adultModeCompatibility || {},
            runtimeFieldCollisionWarnings: entry.runtimeFieldCollisionWarnings || {},
        };
    }
}

const metadata = {
    generatedAt: new Date().toISOString(),
    sourceFiles,
    count: Object.keys(characters).length,
    runtimeFieldDenylist,
};

const output = `/* eslint-disable */
// Generated by scripts/build-re0-character-dossier-static-patch.mjs. Do not edit by hand.
export const characterDossierStaticPatchMetadata = ${JSON.stringify(metadata, null, 4)};

export const characterDossierStaticPatch = ${JSON.stringify(characters, null, 4)};
`;

fs.writeFileSync(path.join(root, outputFile), output);
console.log(JSON.stringify({ outputFile, count: Object.keys(characters).length }, null, 2));
